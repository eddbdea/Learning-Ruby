# Exponent
puts 2 ** 2 # -> 4
puts 4 ** 4 # -> 256

# Modulus
puts 8 % 2 # -> 0
puts 9 % 2 # -> 1

# Converting INT to FLOAT and FLOAT to INT
puts 13.to_f # -> 13.0
puts 13.4.to_i # -> 13

# Useful methods to see if a number is EVEN or ODD
puts 6.even? # -> returns TRUE
puts 7.even? # -> returns FALSE

puts 6.odd? # -> returns FALSE
puts 7.odd? # -> returns TRUE